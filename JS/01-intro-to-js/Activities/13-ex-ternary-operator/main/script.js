// Conditional Statements


let isNightTime = false;

// If / Else syntax
if (!isNightTime) {
  console.log('Turn on the lights!');
} else if (true) {
  console.log('Hello');
} else {
  console.log('Turn off the lights!');
}

// Ternary syntax
isNightTime ? console.log('Turn on the lights!') : console.log('Turn off the lights!');
